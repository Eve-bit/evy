package org.example.examen.Ejercicio1;

public class Calculadora {

    public int suma(int param1, int param2){
        return param1+param2;

    }

    public int resta(int param1, int param2){
        return  param1 - param2;

    }

    public int multiplicacion(int param1, int param2){
        return param1 * param2;
    }

    public  int division(int param1, int param2){
        return param1/param2;

    }
}
